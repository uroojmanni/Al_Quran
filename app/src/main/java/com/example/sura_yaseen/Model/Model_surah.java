package com.example.sura_yaseen.Model;

public class Model_surah {

    int img;
    String text1;

    public Model_surah(int img, String text1) {
        this.img = img;
        this.text1 = text1;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getText1() {
        return text1;
    }

    public void setText1(String text1) {
        this.text1 = text1;
    }
}
